theme.quickAdd = function () {
  $('[data-cart-add]').on('click', function (e) {
    e.preventDefault()
    e.stopPropagation()
    var id = $(this).attr('data-cart-add')

    if (!id) {
      return null
    }

    return ShopifyAPI.quickshop({
      items: [{ quantity: 1, id: id }]
    }, ajaxCart.load)
  })
}

